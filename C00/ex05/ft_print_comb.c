#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	print_numbers_n_times(char a, char b, char c)
{
	ft_putchar(a);
	ft_putchar(b);
	ft_putchar(c);
	if (a != '7' || b != '8' || c != '9')
		write (1, ", ", 2);
}

void	ft_print_comb(void)
{
	char	i;
	char	j;
	char	k;

	i = '0';
	while (i <= '7')
	{
		j = i +1;
		while (j <= '8')
		{
			k = j +1;
			while (k <= '9')
			{
				print_numbers_n_times(i, j, k);
			k++;
			}
		j++;
		}
	i++;
	}
}
